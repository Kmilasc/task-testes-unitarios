export function formatarString(input: string): string {
    if (!input.trim()) {
        return `****************************************************************************************
BEM-VINDO AO MEU PROGRAMA
****************************************************************************************
****************************************************************************************
EU USEI FUNÇÃO PARA FAZER ISSO
****************************************************************************************`;
    }
    
    return `****************************************************************************************
BEM-VINDO AO MEU PROGRAMA
****************************************************************************************
${input}
****************************************************************************************
EU USEI FUNÇÃO PARA FAZER ISSO
****************************************************************************************`;
}
